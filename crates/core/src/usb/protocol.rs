// Common header layout for Line 6 proprietary USB handshake:
//   [0..4]   – LE u32 length field
//   [4..8]   – protocol version marker (01 10 EF 03)
//   [8..10]  – LE u16 sequence number
//   [10]     – command byte
//   [11..]   – payload

